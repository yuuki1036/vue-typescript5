<script setup lang="ts">
import { ref } from 'vue';

// カスタムv-model
const props = defineProps(['modelValue'])
const emits = defineEmits(['update:modelValue'])

const isActiveButton = ref<string>(props.modelValue)

const onClick = (buttonType: string) => {
  isActiveButton.value = buttonType
  emits('update:modelValue', isActiveButton.value)
}
</script>

<template>
<ul>
  <li>
    <button type="button" :class="{active: isActiveButton === 'bad'}" @click="onClick('bad')">Bad</button>
  </li>
  <li>
    <button type="button" :class="{active: isActiveButton === 'ave'}" @click="onClick('ave')">Average</button>
  </li>
  <li>
    <button type="button" :class="{active: isActiveButton === 'grt'}" @click="onClick('grt')">Great</button>
  </li>
</ul>
</template>

